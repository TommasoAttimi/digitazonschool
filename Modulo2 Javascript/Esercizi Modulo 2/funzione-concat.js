// Scrivere una funzione chiamata concat che prende in ingresso due array e ne restituisce uno solo che contenga tutti gli elementi del primo e del secondo array.
// Esempio concat([1,2,3], [4,5,6]) ritorna [1,2,3,4,5,6]

function concat(arr1, arr2) {
  return arr1.concat(arr2);
}
console.log(concat([1, 2, 3], [4, 5, 6]));
